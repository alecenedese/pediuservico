-- ============================================================
-- Schema mínimo de desenvolvimento para rodar o PediuServico
-- (o repositório não inclui o dump original do banco)
-- Reconstruído a partir das queries do código.
-- ============================================================
SET NAMES utf8mb4;
SET FOREIGN_KEY_CHECKS=0;

-- ---------- Navegação (grupos > categoria > prestador) ----------
CREATE TABLE IF NOT EXISTS grupos (
  codigo INT AUTO_INCREMENT PRIMARY KEY,
  titulo VARCHAR(150) NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS categoria (
  codigo INT AUTO_INCREMENT PRIMARY KEY,
  codgrupo INT NOT NULL,
  titulo VARCHAR(150) NOT NULL,
  proxima_fase TINYINT(1) NOT NULL DEFAULT 0
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS subcategoria (
  codigo INT AUTO_INCREMENT PRIMARY KEY,
  codcategoria INT NOT NULL,
  codgrupo INT NULL,
  titulo VARCHAR(150) NOT NULL,
  nome VARCHAR(150) NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS categoria_prestador (
  codigo INT AUTO_INCREMENT PRIMARY KEY,
  codcadastro INT NOT NULL,
  codcategoria INT NOT NULL,
  codsubcategoria INT NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- ---------- Cadastros ----------
CREATE TABLE IF NOT EXISTS parceiro (
  id INT AUTO_INCREMENT PRIMARY KEY,
  TIPO VARCHAR(30) NULL,
  STATUS VARCHAR(30) NULL,
  PERFIL_DO_CLIENTE VARCHAR(60) NULL,
  NOME VARCHAR(150) NULL,
  CNPJ_CPF VARCHAR(30) NULL,
  RG_IE VARCHAR(30) NULL,
  TELEFONE VARCHAR(30) NULL,
  CELULAR VARCHAR(30) NULL,
  CELULAR2 VARCHAR(30) NULL,
  email_pessoal VARCHAR(150) NULL,
  email_comercial VARCHAR(150) NULL,
  ENDERECO VARCHAR(200) NULL,
  NUMERO VARCHAR(30) NULL,
  TIPO_IMOVEL VARCHAR(60) NULL,
  REFERENCIA VARCHAR(200) NULL,
  BAIRRO VARCHAR(120) NULL,
  ESTADO VARCHAR(60) NULL,
  MUNICIPIO VARCHAR(120) NULL,
  COD_IBGE VARCHAR(30) NULL,
  FOTO VARCHAR(255) NULL,
  FOTOCOMDOC VARCHAR(255) NULL,
  senha VARCHAR(255) NULL,
  serviconao TINYINT(1) NULL DEFAULT 0,
  moedas INT NULL DEFAULT 0,
  lat VARCHAR(50) NULL,
  log VARCHAR(50) NULL,
  ultimoAcesso DATETIME NULL,
  dataCad DATETIME NULL DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS clientes (
  id INT AUTO_INCREMENT PRIMARY KEY,
  TIPO VARCHAR(30) NULL,
  NOME VARCHAR(150) NULL,
  CNPJ_CPF VARCHAR(30) NULL,
  TELEFONE VARCHAR(30) NULL,
  CELULAR VARCHAR(30) NULL,
  ESTADO VARCHAR(60) NULL,
  MUNICIPIO VARCHAR(120) NULL,
  senha VARCHAR(255) NULL,
  dataCad DATETIME NULL DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS users (
  id INT AUTO_INCREMENT PRIMARY KEY,
  nome VARCHAR(150) NULL,
  celular VARCHAR(30) NULL,
  senha VARCHAR(255) NULL,
  tipo VARCHAR(30) NULL,
  dataCad DATETIME NULL DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- ---------- Pedidos / fluxo ----------
CREATE TABLE IF NOT EXISTS pedido (
  codigo INT AUTO_INCREMENT PRIMARY KEY,
  id INT NULL,
  codcli INT NULL,
  NOME VARCHAR(150) NULL,
  CNPJ_CPF VARCHAR(30) NULL,
  CELULAR VARCHAR(30) NULL,
  categoria INT NULL,
  subcategoria INT NULL,
  servicos TEXT NULL,
  descricao TEXT NULL,
  audio VARCHAR(255) NULL,
  foto_ VARCHAR(255) NULL,
  availability VARCHAR(60) NULL,
  data_hora DATETIME NULL DEFAULT CURRENT_TIMESTAMP,
  lat VARCHAR(50) NULL,
  log VARCHAR(50) NULL,
  local VARCHAR(200) NULL,
  tempo VARCHAR(60) NULL,
  valor VARCHAR(60) NULL,
  status VARCHAR(30) NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS disparo_pedidos (
  codigo INT AUTO_INCREMENT PRIMARY KEY,
  codpedido INT NULL,
  codcadastro INT NULL,
  aceito VARCHAR(5) NULL DEFAULT 'n',
  visto TINYINT(1) NULL DEFAULT 0,
  style VARCHAR(255) NULL,
  url VARCHAR(255) NULL,
  data_hora DATETIME NULL DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS pega_contato (
  codigo INT AUTO_INCREMENT PRIMARY KEY,
  nome VARCHAR(150) NULL,
  celular VARCHAR(30) NULL,
  codpedido INT NULL,
  codcadastro INT NULL,
  codcliente INT NULL,
  aceito_orcamento VARCHAR(5) NULL,
  data_hora DATETIME NULL DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS markers (
  codigo INT AUTO_INCREMENT PRIMARY KEY,
  nome VARCHAR(150) NULL,
  codcadastro INT NULL,
  valor_min VARCHAR(60) NULL,
  valor_max VARCHAR(60) NULL,
  lat VARCHAR(50) NULL,
  lon VARCHAR(50) NULL,
  type INT NULL,
  codpedido INT NULL,
  qtdestrelas VARCHAR(10) NULL,
  contraproposta VARCHAR(60) NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS avaliacoes (
  codigo INT AUTO_INCREMENT PRIMARY KEY,
  codpedido INT NULL,
  codcadastro INT NULL,
  codcliente INT NULL,
  nota INT NULL,
  comentario TEXT NULL,
  data_hora DATETIME NULL DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS quantidade_pedidos (
  codigo INT AUTO_INCREMENT PRIMARY KEY,
  codcadastro INT NULL,
  quantidade INT NULL DEFAULT 0
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS endereco_prestador (
  codigo INT AUTO_INCREMENT PRIMARY KEY,
  codcadastro INT NULL,
  endereco VARCHAR(200) NULL,
  lat VARCHAR(50) NULL,
  lon VARCHAR(50) NULL,
  principal TINYINT(1) NULL DEFAULT 0
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS moedas_extrato (
  codigo INT AUTO_INCREMENT PRIMARY KEY,
  codcadastro INT NULL,
  tipo VARCHAR(30) NULL,
  valor INT NULL,
  descricao VARCHAR(200) NULL,
  data_hora DATETIME NULL DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS push_tokens (
  id INT AUTO_INCREMENT PRIMARY KEY,
  codcadastro INT NULL,
  token TEXT NULL,
  endpoint TEXT NULL,
  p256dh TEXT NULL,
  auth TEXT NULL,
  tipo VARCHAR(30) NULL,
  data_hora DATETIME NULL DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS push_fila (
  id INT AUTO_INCREMENT PRIMARY KEY,
  codcadastro INT NULL,
  titulo VARCHAR(150) NULL,
  mensagem TEXT NULL,
  status VARCHAR(20) NULL DEFAULT 'pendente',
  data_hora DATETIME NULL DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS timer_acordo (
  codigo INT AUTO_INCREMENT PRIMARY KEY,
  codpedido INT NULL,
  codcadastro INT NULL,
  inicio DATETIME NULL,
  fim DATETIME NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS verificacoes_usuario (
  id INT AUTO_INCREMENT PRIMARY KEY,
  celular VARCHAR(30) NULL,
  codigo VARCHAR(10) NULL,
  verificado TINYINT(1) NULL DEFAULT 0,
  data_hora DATETIME NULL DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS verification_codes (
  id INT AUTO_INCREMENT PRIMARY KEY,
  phone VARCHAR(30) NULL,
  code VARCHAR(10) NULL,
  created_at DATETIME NULL DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS chats (
  id INT AUTO_INCREMENT PRIMARY KEY,
  codpedido INT NULL,
  remetente VARCHAR(30) NULL,
  mensagem TEXT NULL,
  data_hora DATETIME NULL DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS pagamento (
  id INT AUTO_INCREMENT PRIMARY KEY,
  codcadastro INT NULL,
  valor VARCHAR(30) NULL,
  txid VARCHAR(100) NULL,
  status VARCHAR(30) NULL,
  data_hora DATETIME NULL DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS edicao (
  id INT AUTO_INCREMENT PRIMARY KEY,
  codcadastro INT NULL,
  campo VARCHAR(60) NULL,
  valor TEXT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS denuncias (
  id INT AUTO_INCREMENT PRIMARY KEY,
  codcadastro INT NULL,
  codpedido INT NULL,
  motivo TEXT NULL,
  data_hora DATETIME NULL DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS estados (
  id INT AUTO_INCREMENT PRIMARY KEY,
  uf VARCHAR(2) NULL,
  nome VARCHAR(60) NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS cidades (
  id INT AUTO_INCREMENT PRIMARY KEY,
  uf VARCHAR(2) NULL,
  nome VARCHAR(120) NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS config_app (
  id INT AUTO_INCREMENT PRIMARY KEY,
  chave VARCHAR(60) NULL,
  valor TEXT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

SET FOREIGN_KEY_CHECKS=1;
